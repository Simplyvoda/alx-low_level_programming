#include <stdio.h>
#include <stdlib.h>

/*
 * add - adds two integers
 * @a - first integer
 * @b - second integer
 */
int add(a, b)
{
	return (a + b);
}

/*
 * sub - function to find differnece of two integers
 * @a - first integer
 * @b - second integer
 */

int sub(a, b)
{
        return (a - b);
}

/*
 * mul - multiplies two integers
 * @a - first integer
 * @b - second integer
 */

int mul(a, b)
{
        return (a * b);
}

/*
 * div - function that divides two integers
 * @a - first integer
 * @b - second integer
 */

int div(a, b)
{
        return (a / b);
}

/*
 * mod - modulus of two integers
 * @a - first integer
 * @b - second integer
 */

int mod(a, b)
{
        return (a % b);
}
