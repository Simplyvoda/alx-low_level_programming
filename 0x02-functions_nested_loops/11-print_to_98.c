#include <stdio.h>

/**
 * print_to_98 - Prints numbers from n to 98.
 *
 * @n: Theinput number.
 *
 * Return: no return.
 */

#include <stdio.h>

void print_to_98(int n);

void print_to_98(int n)
{ 
  int i;
  for (i = n; i < 98; i++)
  {
      printf("%d, ", i);
  }
  if (i = 98)
  {
      printf("%d\n", i);
  }
}

